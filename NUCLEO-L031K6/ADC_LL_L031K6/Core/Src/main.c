/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  LL_APB2_GRP1_EnableClock(LL_APB2_GRP1_PERIPH_SYSCFG);
  LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_PWR);

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC_Init();
  /* USER CODE BEGIN 2 */

  LL_ADC_StartCalibration(ADC1);
  while (LL_ADC_IsCalibrationOnGoing(ADC1));  // Wait until calibration is complete

  LL_ADC_Enable(ADC1);
  while (!LL_ADC_IsEnabled(ADC1)); // Wait until ADC is ready

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  LL_GPIO_SetOutputPin(ADC_CONV_GPIO_Port, ADC_CONV_Pin);
	// Start ADC conversion
	LL_ADC_REG_StartConversion(ADC1);

	// Poll for conversion completion
	while (!LL_ADC_IsActiveFlag_EOC(ADC1));  // Wait for conversion to complete
	adc_res1 = LL_ADC_REG_ReadConversionData12(ADC1);

	 LL_GPIO_ResetOutputPin(ADC_CONV_GPIO_Port, ADC_CONV_Pin);
	 LL_ADC_ClearFlag_EOC(ADC1);

	// Start another ADC conversion
	 LL_GPIO_SetOutputPin(ADC_CONV_GPIO_Port, ADC_CONV_Pin);
	LL_ADC_REG_StartConversion(ADC1);

	// Poll for conversion completion
	while (!LL_ADC_IsActiveFlag_EOC(ADC1));  // Wait for conversion to complete
	adc_res2 = LL_ADC_REG_ReadConversionData12(ADC1);
	LL_GPIO_ResetOutputPin(ADC_CONV_GPIO_Port, ADC_CONV_Pin);

	// Clear EOC (End of Conversion) flag
	LL_ADC_ClearFlag_EOC(ADC1);

	// Calculate voltage values based on ADC readings
	adc_res2_V = VDDA_CHARAC * (*VREFINT_CAL) / adc_res2;
	adc_res1_V = (adc_res2_V / 4095.0) * adc_res1;

	// Add delay
	LL_mDelay(500);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

